package com.nature.feeds.bean;

public class CollectionBean {

    private String isbn;
    private String productDesc;
    private String productGroupDesc;
    private String collectionAcronym;
    private String collectionIsbn;
    private String collectionWorkid;

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getProductDesc() {
        return productDesc;
    }

    public void setProductDesc(String productDesc) {
        this.productDesc = productDesc;
    }

    public String getProductGroupDesc() {
        return productGroupDesc;
    }

    public void setProductGroupDesc(String productGroupDesc) {
        this.productGroupDesc = productGroupDesc;
    }

    public String getCollectionAcronym() {
        return collectionAcronym;
    }

    public void setCollectionAcronym(String collectionAcronym) {
        this.collectionAcronym = collectionAcronym;
    }

    public String getCollectionIsbn() {
        return collectionIsbn;
    }

    public void setCollectionIsbn(String collectionIsbn) {
        this.collectionIsbn = collectionIsbn;
    }

    public String getCollectionWorkid() {
        return collectionWorkid;
    }

    public void setCollectionWorkid(String collectionWorkid) {
        this.collectionWorkid = collectionWorkid;
    }
}
